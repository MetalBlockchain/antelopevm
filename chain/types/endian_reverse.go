package types

func EndianReverseU64(x uint64) uint64 {
	return ((x >> 0x38) & 0xFF) |
		(((x >> 0x30) & 0xFF) << 0x08) |
		(((x >> 0x28) & 0xFF) << 0x10) |
		(((x >> 0x20) & 0xFF) << 0x18) |
		(((x >> 0x18) & 0xFF) << 0x20) |
		(((x >> 0x10) & 0xFF) << 0x28) |
		(((x >> 0x08) & 0xFF) << 0x30) |
		(((x) & 0xFF) << 0x38)
}
func EndianReverseU32(x uint32) uint32 {
	return ((x >> 0x18) & 0xFF) |
		(((x >> 0x10) & 0xFF) << 0x08) |
		(((x >> 0x08) & 0xFF) << 0x10) |
		(((x) & 0xFF) << 0x18)

}
